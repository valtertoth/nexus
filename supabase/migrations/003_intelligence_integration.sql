-- ─── 8. Analytics views ───────────────────────────────────────────────────────

-- Conversion attribution summary: ROAS-ready view
CREATE OR REPLACE VIEW v_conversion_attribution AS
SELECT
  c.org_id,
  c.attr_channel,
  c.attr_campaign,
  c.attr_campaign_id,
  c.attr_ad_id,
  COUNT(*)                                               AS total_conversations,
  COUNT(*) FILTER (WHERE c.outcome = 'converted')       AS conversions,
  COUNT(*) FILTER (WHERE c.outcome = 'lost')            AS lost,
  COUNT(*) FILTER (WHERE c.outcome = 'problem')         AS problems,
  SUM(c.outcome_value) FILTER (WHERE c.outcome = 'converted') AS revenue,
  AVG(c.outcome_value) FILTER (WHERE c.outcome = 'converted') AS avg_order_value,
  ROUND(
    COUNT(*) FILTER (WHERE c.outcome = 'converted')::NUMERIC
    / NULLIF(COUNT(*), 0) * 100, 1
  )                                                      AS conversion_rate_pct
FROM conversations c
WHERE c.outcome IS NOT NULL
GROUP BY c.org_id, c.attr_channel, c.attr_campaign, c.attr_campaign_id, c.attr_ad_id;

-- Top winning patterns by sector
CREATE OR REPLACE VIEW v_top_winning_patterns AS
SELECT
  ci.org_id,
  ci.sector_id,
  s.name AS sector_name,
  ci.insight_type,
  ci.title,
  ci.description,
  ci.playbook,
  ci.example_quote,
  ci.confidence,
  ci.tags,
  ci.created_at
FROM conversation_insights ci
LEFT JOIN sectors s ON s.id = ci.sector_id
WHERE ci.insight_type IN ('winning_pattern', 'key_phrase', 'objection_handled')
  AND ci.is_active = TRUE
  AND ci.confidence >= 0.7
ORDER BY ci.confidence DESC, ci.created_at DESC;
